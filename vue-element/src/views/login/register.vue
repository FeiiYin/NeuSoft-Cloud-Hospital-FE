<script>
// import { validUsername } from '@/utils/validate'
import SocialSign from './components/SocialSignin'

import {
  addAccount
} from '../../api/basicInfo/account'

export default {
  name: 'Login',
  components: { SocialSign },
  data() {
    // const validateUsername = (rule, value, callback) => {
    //   if (!validUsername(value)) {
    //     callback(new Error('Please enter the correct user name'))
    //   } else {
    //     callback()
    //   }
    // }
    const validatePassword = (rule, value, callback) => {
      if (value.length < 6) {
        callback(new Error('The password can not be less than 6 digits'))
      } else {
        callback()
      }
    }
    return {
      loginForm: {
        username: '',
        password: '',
        realname: '',
        accountScope: []
      },
      accountScope: [],
      val: '门诊医生',
      loginRules: {
        username: [{ required: true, trigger: 'blur' }],
        realname: [{ required: true, trigger: 'blur' }],
        password: [{ required: true, trigger: 'blur', validator: validatePassword }]
      },
      passwordType: 'password',
      capsTooltip: false,
      loading: false,
      showDialog: false,
      redirect: undefined,
      otherQuery: {},

      options: [
        {
          value: '门诊医生',
          label: '门诊医生'
        }, {
          value: '医技医生',
          label: '医技医生'
        }, {
          value: '医院管理员',
          label: '医院管理员'
        }, {
          value: '药房操作员',
          label: '药房操作员'
        }, {
          value: '财务管理员',
          label: '财务管理员'
        }, {
          value: '挂号收费员',
          label: '挂号收费员'
        }
      ]
    }
  },
  watch: {
    $route: {
      handler: function(route) {
        const query = route.query
        if (query) {
          this.redirect = query.redirect
          this.otherQuery = this.getOtherQuery(query)
        }
      },
      immediate: true
    }
  },
  created() {
    // window.addEventListener('storage', this.afterQRScan)
  },
  mounted() {
    if (this.loginForm.username === '') {
      this.$refs.username.focus()
    } else if (this.loginForm.password === '') {
      this.$refs.password.focus()
    }
  },
  destroyed() {
    // window.removeEventListener('storage', this.afterQRScan)
  },
  methods: {
    checkCapslock({ shiftKey, key } = {}) {
      if (key && key.length === 1) {
        if (shiftKey && (key >= 'a' && key <= 'z') || !shiftKey && (key >= 'A' && key <= 'Z')) {
          this.capsTooltip = true
        } else {
          this.capsTooltip = false
        }
      }
      if (key === 'CapsLock' && this.capsTooltip === true) {
        this.capsTooltip = false
      }
    },
    showPwd() {
      if (this.passwordType === 'password') {
        this.passwordType = ''
      } else {
        this.passwordType = 'password'
      }
      this.$nextTick(() => {
        this.$refs.password.focus()
      })
    },
    handleLogin() {
      this.$refs.loginForm.validate(valid => {
        if (valid) {
          this.loading = true
          this.accountScope = []
          var val = this.val
          if (val === '门诊医生') {
            this.accountScope.push('00')
          } else if (val === '医技医生') {
            this.accountScope.push('01')
          } else if (val === '医院管理员') {
            this.accountScope.push('10')
          } else if (val === '药房操作员') {
            this.accountScope.push('11')
          } else if (val === '财务管理员') {
            this.accountScope.push('12')
          } else if (val === '挂号收费员') {
            this.accountScope.push('13')
          }
          var query = {
            userName: this.loginForm.username,
            userPassword: this.loginForm.password,
            realName: this.loginForm.realname,
            departmentId: '',
            accountType: this.accountScope[0],
            jobTitle: '',
            doctorScheduling: ''
          }
          console.log(query)
          addAccount(query).then(response => {
            console.log('addAccount response: ')
            console.log(response)
            this.loading = false
            if (response.data === false) {
              this.$message.error('注册失败，用户名已经被注册！')
            } else {
              this.$message({
                message: '注册失败，用户名已经被注册！',
                type: 'success'
              })
              this.$router.push({
                path: '/login'
              })
            }
          }).catch(error => {
            console.log(error)
          })
        }
      })
    },
    getOtherQuery(query) {
      return Object.keys(query).reduce((acc, cur) => {
        if (cur !== 'redirect') {
          acc[cur] = query[cur]
        }
        return acc
      }, {})
    }
    // afterQRScan() {
    //   if (e.key === 'x-admin-oauth-code') {
    //     const code = getQueryObject(e.newValue)
    //     const codeMap = {
    //       wechat: 'code',
